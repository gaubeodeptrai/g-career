    function company($_=array('w_id'=>'','w_name'=>'')) {

        $where = array();        
        if(!empty($_["w_id"])){
            $id = DB::quote($_["w_id"]);
            $where[] = "id = {$id}";
        }
        if(!empty($_["w_name"])){
            $name = DB::quote($_["w_name"]);
            $where[] = "name = {$name}";
        }
        $where = implode(" and ", $where);
        $where = (!empty($where))?"where {$where}":"";
        
        $sql = "select
        sql_calc_found_rows
        *
        from _WK_company
        {$where}
        ";
        
        $d = array();
        
        if(
        DB::query($sql)
        ){
            while($r = DB::fetch()){
                $d[$r["id"]] = $r;
            }
            
            $sql_ct = "select found_rows() as ct";
            $res_ct = DB::query($sql_ct);
            $row_ct = DB::fetch($res_ct);
            
            DB::fetchClose();
            DataCvt::$dataTotal = $row_ct["ct"];
        }
        
        return $d;
        
    }

