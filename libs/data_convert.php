    //駅（路線・駅該当）
    function stationsCnv($_=array('w_id'=>'','w_id_n'=>'')) {

        $where = array();        
        if(!empty($_["w_id"])){
            $id = DB::quote($_["w_id"]);
            $where[] = "_WK_station.id = {$id}";
        }

        if(!empty($_["w_id_n"])){
            $id_n = DB::quote($_["w_id_n"]);
            $where[] = "_station.station_cd = {$id_n}";
        }
        
        $where = implode(" and ", $where);
        $where = (!empty($where))?"where {$where}":"";
        
        $sql = "select
        distinct
        sql_calc_found_rows
        _WK_station.id as ids,
        _station.station_cd as id,
        _station.station_name as name,
        _line.line_name_h as linename
        from 
        (_station
        inner join _line on _line.line_cd = _station.line_cd
        )  inner join _WK_station on (_WK_station.station_name = _station.station_name collate utf8_unicode_ci) and (_line.line_name_h like concat('%',_WK_station.railroad_name,'%'))
        {$where}
        ";
        
        
        $d = array();
        
        if(
        DB::query($sql)
        ){
            if(!empty($_["w_id"])){
                while($r = DB::fetch()){
                    $d[$r["ids"]] = $r;
                }
            } else {
                while($r = DB::fetch()){
                    $d[$r["id"]] = $r;
                }
            }
            
            $sql_ct = "select found_rows() as ct";
            $res_ct = DB::query($sql_ct);
            $row_ct = DB::fetch($res_ct);
            
            DB::fetchClose();
            DataCvt::$dataTotal = $row_ct["ct"];
        }
        
        return $d;
        
    }

